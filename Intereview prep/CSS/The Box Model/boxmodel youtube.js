/*
4 layers:
margin
border
padding
content

- content lives in the center


padding- represents the space between the content and the border of the content.
- gives more room around the content
- not meant to move the content around
- used for internal space

border- seperates the content and the padding from the margin.
- border is the divider between the padding and margin
- can be styles with a css property called border
- by default borders are invisible
- doesnt create any spacing unless you want it to

margin- the space between the border and all the other content
- margin moves the content around and gives it space between other elements
- margin is used for external space

- if you have an image and some text under it you can use the margin to create space between the text and the image



































*/
